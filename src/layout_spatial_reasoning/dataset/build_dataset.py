"""
Build the Form Layout Benchmark (FLB) dataset.

Generates 1,600 unique FormSpec records:
  8 domains × 5 scenarios × 40 forms per scenario = 200 forms per domain.

Controls are randomly shuffled per Section 3.3.2 of the thesis.
Sources: USWDS, GOV.UK, GSA, IRS (base templates);
         Jotform, Typeform, Cognito Forms (inspiration).
"""
from __future__ import annotations

import random
from dataclasses import dataclass, field
from pathlib import Path
from typing import NamedTuple

from layout_spatial_reasoning.schemas.control import Control
from layout_spatial_reasoning.schemas.form import FormSpec, OrderConstraint


FORMS_PER_SCENARIO: int = 40
SCENARIOS_PER_DOMAIN: int = 5
FORMS_PER_DOMAIN: int = FORMS_PER_SCENARIO * SCENARIOS_PER_DOMAIN   # 200
TOTAL_FORMS: int = FORMS_PER_DOMAIN * 8                              # 1 600


class CT(NamedTuple):
    """Compact control template (label, type, optional help_text)."""

    label: str
    type: str
    help_text: str = ""


@dataclass
class Scenario:
    id: str
    domain: str
    min_fields: int
    max_fields: int
    required: list[CT]
    optional: list[CT]
    order_hints: list[tuple[str, str]] = field(default_factory=list)


# ─────────────────────────── generator ───────────────────────────

def _pick(sc: Scenario, rng: random.Random) -> list[CT]:
    pool = list(sc.optional)
    rng.shuffle(pool)
    target = rng.randint(sc.min_fields, sc.max_fields)
    n = min(max(0, target - len(sc.required)), len(pool))
    return list(sc.required) + pool[:n]


def _make_form(sc: Scenario, idx: int, rng: random.Random) -> FormSpec:
    cts = _pick(sc, rng)
    rng.shuffle(cts)
    controls = [
        Control(id=f"c{i + 1:02d}", label=ct.label, type=ct.type, help_text=ct.help_text)
        for i, ct in enumerate(cts)
    ]
    lbl = {c.label: c.id for c in controls}
    constraints = [
        OrderConstraint(before=lbl[a], after=lbl[b])
        for a, b in sc.order_hints
        if a in lbl and b in lbl
    ]
    return FormSpec(
        form_id=f"{sc.id}_{idx:03d}",
        domain=sc.domain,
        controls=controls,
        order_constraints=constraints,
        target_layouts=[],
    )


def _forms_for(sc: Scenario, count: int, rng: random.Random) -> list[FormSpec]:
    seen: set[frozenset[str]] = set()
    forms: list[FormSpec] = []
    idx = attempts = 0
    while len(forms) < count:
        attempts += 1
        if attempts > count * 500:
            raise RuntimeError(
                f"Scenario '{sc.id}': only {len(forms)}/{count} unique forms generated. "
                "Increase the optional pool size."
            )
        idx += 1
        form = _make_form(sc, idx, rng)
        key = frozenset(c.label for c in form.controls)
        if key not in seen:
            seen.add(key)
            forms.append(form)
    return forms


def build_dataset(output_path: str | Path, seed: int = 42) -> list[FormSpec]:
    """Generate the FLB dataset and write it to a JSONL file."""
    rng = random.Random(seed)
    all_forms: list[FormSpec] = []
    for domain_scenarios in _SCENARIOS.values():
        for sc in domain_scenarios:
            all_forms.extend(_forms_for(sc, FORMS_PER_SCENARIO, rng))

    out = Path(output_path)
    out.parent.mkdir(parents=True, exist_ok=True)
    with out.open("w", encoding="utf-8") as f:
        for form in all_forms:
            f.write(form.model_dump_json())
            f.write("\n")

    print(f"Wrote {len(all_forms)} forms → {out}")
    return all_forms


